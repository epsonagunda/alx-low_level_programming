#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
*op_add - function to add two numbers
*@a: first num
*@b: num two
*Return: sum of tw numbers
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
*op_sub - function to subtract two numbers
*@a: first num
*@b: num two
*Return: difference of two numbers
*/
int op_sub(int a, int b)
{
return (a - b);
}


/**
*op_mul - function to calculate the product of two numbers
*@a: first num
*@b: num two
*Return: product of two numbers
*/
int op_mul(int a, int b)
{
return (a * b);
}


/**
*op_div - function to divid two numbers
*@a: first num
*@b: num two
*Return: result of division of two numbers
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
*op_mod - function to return remainder of two numbers
*@a: first num
*@b: num two
*Return: remainder of two numbers
*/
int op_mod(int a, int b)
{
return (a % b);
}

