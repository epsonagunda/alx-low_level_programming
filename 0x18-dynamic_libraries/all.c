#include <stdlib.h>
/**
*add - addition of two numbers
*@a: first int
*@b: second int
Return: sum of two number
*/
int add(int a, int b)
{
return (a + b);
}

/**
*sub  - subtraction of two numbers
*@a: first int
*@b: second int
*Return: subtraction of two number
*/
int sub(int a, int b)
{
return (a - b);
}

/**
*mul - multiplication of two numbers
*@a: first int
*@b: second int
*Return: product of two number
*/
int mul(int a, int b)
{
return (a * b);
}

/**
*mod - mode of two numbers
*@a: first int
*@b: second int
*Return: mode of two number
*/
int mod(int a, int b)
{
return (a % b);
}
